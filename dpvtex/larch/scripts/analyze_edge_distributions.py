#!/usr/bin/env python3
"""
Analysis script to compare non-MP edge distributions across different edge_distribution methods.

This script loads the pickle files generated by the larch pipeline with different edge_distribution 
settings and plots the number of non-MP edges (label=1) in each dataset for comparison.
"""

import pickle
import matplotlib.pyplot as plt
import seaborn as sns
import pandas as pd
import numpy as np
from pathlib import Path
import argparse


def load_tree_label_data(pickle_file):
    """
    Load tree and label data from a pickle file.
    
    Args:
        pickle_file: Path to pickle file containing tree-label dictionary
        
    Returns:
        dict: Dictionary with trees as keys and edge label lists as values
    """
    try:
        with open(pickle_file, 'rb') as f:
            data = pickle.load(f)
        return data
    except FileNotFoundError:
        print(f"Warning: File {pickle_file} not found")
        return {}
    except Exception as e:
        print(f"Error loading {pickle_file}: {e}")
        return {}


def analyze_edge_distributions(data_dict, method_name):
    """
    Analyze the distribution of non-MP edges in the data.
    
    Args:
        data_dict: Dictionary with trees as keys and edge label lists as values
        method_name: String name of the edge distribution method
        
    Returns:
        dict: Summary statistics
    """
    if not data_dict:
        return {
            'method': method_name,
            'num_trees': 0,
            'mean_non_mp_edges': 0,
            'std_non_mp_edges': 0,
            'min_non_mp_edges': 0,
            'max_non_mp_edges': 0,
            'total_edges_per_tree': 0,
            'non_mp_edge_counts': []
        }
    
    non_mp_counts = []
    total_edges_per_tree = []
    
    for tree, labels in data_dict.items():
        non_mp_count = sum(labels)  # Count of 1s (non-MP edges)
        total_edges = len(labels)
        
        non_mp_counts.append(non_mp_count)
        total_edges_per_tree.append(total_edges)
    
    return {
        'method': method_name,
        'num_trees': len(data_dict),
        'mean_non_mp_edges': np.mean(non_mp_counts) if non_mp_counts else 0,
        'std_non_mp_edges': np.std(non_mp_counts) if non_mp_counts else 0,
        'min_non_mp_edges': np.min(non_mp_counts) if non_mp_counts else 0,
        'max_non_mp_edges': np.max(non_mp_counts) if non_mp_counts else 0,
        'mean_total_edges': np.mean(total_edges_per_tree) if total_edges_per_tree else 0,
        'non_mp_edge_counts': non_mp_counts,
        'proportion_non_mp': np.mean(non_mp_counts) / np.mean(total_edges_per_tree) if total_edges_per_tree else 0
    }


def create_comparison_plots(analysis_results, output_dir="plots"):
    """
    Create comparison plots for different edge distribution methods.
    
    Args:
        analysis_results: List of analysis result dictionaries
        output_dir: Directory to save plots
    """
    Path(output_dir).mkdir(exist_ok=True)
    
    # Filter out empty results
    valid_results = [r for r in analysis_results if r['num_trees'] > 0]
    
    if not valid_results:
        print("No valid data to plot")
        return
    
    # Set up the plotting style
    plt.style.use('default')
    sns.set_palette("husl")
    
    # Create figure with subplots
    fig, axes = plt.subplots(2, 2, figsize=(15, 12))
    fig.suptitle('Comparison of Non-MP Edge Distributions Across Methods', fontsize=16)
    
    # 1. Bar plot of mean non-MP edges
    methods = [r['method'] for r in valid_results]
    means = [r['mean_non_mp_edges'] for r in valid_results]
    stds = [r['std_non_mp_edges'] for r in valid_results]
    
    axes[0,0].bar(methods, means, yerr=stds, capsize=5, alpha=0.7)
    axes[0,0].set_title('Mean Number of Non-MP Edges by Method')
    axes[0,0].set_ylabel('Mean Non-MP Edges')
    axes[0,0].tick_params(axis='x', rotation=45)
    
    # 2. Bar plot of proportions
    proportions = [r['proportion_non_mp'] for r in valid_results]
    axes[0,1].bar(methods, proportions, alpha=0.7)
    axes[0,1].set_title('Proportion of Non-MP Edges by Method')
    axes[0,1].set_ylabel('Proportion Non-MP Edges')
    axes[0,1].tick_params(axis='x', rotation=45)
    
    # 3. Box plot of distributions
    all_counts = []
    all_methods = []
    for result in valid_results:
        counts = result['non_mp_edge_counts']
        method = result['method']
        all_counts.extend(counts)
        all_methods.extend([method] * len(counts))
    
    if all_counts:
        df = pd.DataFrame({'Method': all_methods, 'Non_MP_Edges': all_counts})
        sns.boxplot(data=df, x='Method', y='Non_MP_Edges', ax=axes[1,0])
        axes[1,0].set_title('Distribution of Non-MP Edge Counts')
        axes[1,0].tick_params(axis='x', rotation=45)
    
    # 4. Summary statistics table
    axes[1,1].axis('off')
    table_data = []
    for result in valid_results:
        table_data.append([
            result['method'],
            f"{result['num_trees']}",
            f"{result['mean_non_mp_edges']:.1f}",
            f"{result['std_non_mp_edges']:.1f}",
            f"{result['proportion_non_mp']:.3f}"
        ])
    
    table = axes[1,1].table(
        cellText=table_data,
        colLabels=['Method', 'Trees', 'Mean\nNon-MP', 'Std\nNon-MP', 'Proportion\nNon-MP'],
        cellLoc='center',
        loc='center'
    )
    table.auto_set_font_size(False)
    table.set_fontsize(10)
    table.scale(1, 2)
    axes[1,1].set_title('Summary Statistics')
    
    plt.tight_layout()
    plt.savefig(f'{output_dir}/edge_distribution_comparison.png', dpi=300, bbox_inches='tight')
    plt.savefig(f'{output_dir}/edge_distribution_comparison.pdf', bbox_inches='tight')
    print(f"Saved comparison plots to {output_dir}/")
    
    return fig


def main():
    parser = argparse.ArgumentParser(description='Analyze edge distributions from larch pipeline output')
    parser.add_argument('--data_dir', default='shared_data', 
                        help='Directory containing the larch output pickle files')
    parser.add_argument('--output_dir', default='plots', 
                        help='Directory to save analysis plots')
    parser.add_argument('--dataset_name', default='alisim_alignment_15_seq_50_sites_5_algnmnts',
                        help='Dataset name prefix')
    
    args = parser.parse_args()
    
    # Define the edge distribution methods to analyze
    edge_methods = ['constant', 'uniform', 'treesearch', 'random_subtree']
    
    # Load data for each method
    analysis_results = []
    
    for method in edge_methods:
        if method == 'constant':
            suffix = '_spr'
        elif method == 'uniform':
            suffix = '_uniform'
        elif method == 'treesearch':
            suffix = '_treesearch'
        elif method == 'random_subtree':
            suffix = '_subtree'
        
        pickle_file = f"{args.data_dir}/larch_{args.dataset_name}{suffix}.p"
        
        print(f"Loading data for {method} method from {pickle_file}")
        data = load_tree_label_data(pickle_file)
        
        if data:
            analysis = analyze_edge_distributions(data, method)
            analysis_results.append(analysis)
            
            print(f"  {method}: {analysis['num_trees']} trees, "
                  f"mean non-MP edges: {analysis['mean_non_mp_edges']:.1f} ± {analysis['std_non_mp_edges']:.1f}")
        else:
            print(f"  {method}: No data found")
            # Add empty result for completeness
            analysis_results.append(analyze_edge_distributions({}, method))
    
    # Create comparison plots
    if analysis_results:
        print("\\nCreating comparison plots...")
        create_comparison_plots(analysis_results, args.output_dir)
        
        # Print summary
        print("\\n" + "="*50)
        print("SUMMARY OF EDGE DISTRIBUTION ANALYSIS")
        print("="*50)
        
        for result in analysis_results:
            if result['num_trees'] > 0:
                print(f"{result['method'].upper()}: {result['num_trees']} trees, "
                      f"{result['mean_non_mp_edges']:.1f} ± {result['std_non_mp_edges']:.1f} non-MP edges "
                      f"({result['proportion_non_mp']:.1%} of total edges)")
            else:
                print(f"{result['method'].upper()}: No data available")
    else:
        print("No data found for analysis")


if __name__ == "__main__":
    main()