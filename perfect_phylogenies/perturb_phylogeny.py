from ete3 import Tree
from utils import populate
from random import randrange, choice
from historydag.parsimony import disambiguate, parsimony_score


def perturb_tree(tree, depth, skip_root=True, exception_on_fail=False):
    """
    Returns a new ete3 Tree instance built from the input tree with a subtree of the
    specified depth replaced with a random tree, where "depth" means distance to
    farthest leaf. The subtree is selected uniformly from the subtrees of required depth
    that consist of an internal node as the root and all nodes below this root to the
    specified depth. Leaf nodes of this subtree are unaltered.

    Parameters:
        tree (ete3.Tree): The input tree.
        depth (int): The depth of the subtree to replace with a random tree.
        skip_root (bool): When True, the subtree does not begin at the root of the tree.
        exception_on_fail (bool): When True, the method raises a ValueError exception
            when the tree does not contain a subtree of the given depth. Otherwise, the
            method returns None in such cases.
    """
    if tree is None:
        if exception_on_fail:
            raise ValueError("Tree cannot be None.")
        else:
            return None
    if depth < 1:
        raise ValueError("Depth must be at least 1.")

    tree = tree.copy()
    any(node.add_feature("random_tree", False) for node in tree.traverse())
    valid_nodes = [
        node
        for node in tree.traverse(strategy="preorder")
        if not (skip_root and node.is_root()) and depth <= tree_depth(node)
    ]
    n = len(valid_nodes)
    if n == 0:
        if exception_on_fail:
            raise ValueError("Input tree has no subtree of required depth.")
        else:
            return None

    selected_node = choice(valid_nodes)
    parent_node = None if selected_node.is_root() else selected_node.up
    selected_node.detach()
    is_sorta_tip = lambda x: is_subtree_depth_tip(selected_node, x, depth)
    sorta_tips = [n.detach() for n in selected_node.get_leaves(is_leaf_fn=is_sorta_tip)]
    new_subtree = make_random_tree(sorta_tips)

    if parent_node is None:
        tree = new_subtree
    else:
        parent_node.add_child(new_subtree)

    return tree


def tree_depth(node):
    """
    Returns the depth of the tree with the given node as the root. This depth is the
    number of nodes along the longest path from the given node to a leaf node.
    """
    return node.get_farthest_leaf(topology_only=True)[1] + 1


def edge_distance(node1, node2):
    """Returns the number of edges between the nodes."""
    return node1.get_distance(node2, topology_only=True) + 1


def is_subtree_depth_tip(root_node, other_node, depth):
    """
    Returns the truth value of other_node being a tip of the subtree at root_node of the
    given depth. The tips are either exactly depth nodes away from root_node or are
    leaf nodes that are fewer than depth nodes away. This method assumes other_node is a
    descendent of root_node.
    """
    distance = edge_distance(root_node, other_node)
    return (depth == distance) or (depth > distance and other_node.is_leaf())


def make_random_tree(tip_subtrees):
    """
    Returns an ete3 Tree with the entries of tip_subtrees placed at the leaves. The tree
    is randomly generated by the utils.py-defined populate method with 'uniform' model.
    The randomly generated tree is made with len(tip_subtrees) leaf nodes, which are
    then replaced with with the nodes of tip_subtrees. The non-root nodes of the tree
    are marked with the node attribute random_tree=True.
    """
    leaf_count = len(tip_subtrees)
    tree = Tree()
    populate(tree, leaf_count, model="uniform")
    for old_leaf, new_leaf in zip(tree.get_leaves(), tip_subtrees):
        assert old_leaf.up is not None
        old_leaf.up.add_child(new_leaf)
        old_leaf.delete(prevent_nondicotomic=False)
    any(node.add_feature("random_tree", True) for node in tree.get_descendants())
    tree.add_feature("random_tree", False)
    return tree


def sankoff_for_missing_sequences(tree):
    """
    Fills in the sequence attribute of the nodes in tree where the attribute is missing.
    Sequences are determined by the Sankoff algorithm and nodes are modified in-place.
    """
    if tree is None:
        return None
    some_leaf = tree.get_leaves()[0]
    if not hasattr(some_leaf, "sequence"):
        raise ValueError("Set leaf sequences first.")
    site_count = len(some_leaf.sequence)
    fake_sequence = "N" * site_count

    # The next few lines are a silly python trick. This is the current fast way to
    # apply a function to items in an iterable, where the function modifies the items
    # in-place and returns None. Usually faster than a for-loop.
    any(
        node.add_feature("sequence", fake_sequence)
        for node in tree.traverse(strategy="preorder")
        if not hasattr(node, "sequence")
    )

    disambiguate(tree, use_internal_node_sequences=True)
    return None


def make_worse_tree(tree, depth, max_attempts=100):
    """
    Attempt to replace a random subtree of the given depth of the tree, with the new
    tree having a higher parsimony score.
    """
    old_score = parsimony_score(tree)
    perturbed_tree = None
    for _ in range(max_attempts):
        perturbed_tree = perturb_tree(tree, depth, exception_on_fail=False)
        if perturbed_tree is None:
            return None
        sankoff_for_missing_sequences(perturbed_tree)
        if parsimony_score(perturbed_tree) > old_score:
            return perturbed_tree
    return None
