# Configuration constants

# Map internal method names to display names for plot legends
METHOD_NAME_MAP = {"constant": "SPR", "random_subtree": "random subtree"}


def get_pickle_pattern(method):
    """Get regex pattern to match pickle files for an edge distribution method.

    Matches both old-style suffixes (e.g., _spr.p) and new-style with parameters
    (e.g., _spr_r2_t0.1.p, _subtree_t0.167.p).
    """
    if method == "mixed":
        return r"_spr_subtree_few_sprs\.p$"
    elif method == "constant":
        return r"_spr(_r[^_]+_t[^_]+)?\.p$"
    elif method == "random_subtree":
        return r"_subtree(_t[^_]+)?\.p$"
    elif method == "uniform":
        return r"_uniform(_r[^_]+_t[^_]+)?\.p$"
    elif method == "treesearch_mimic":
        return r"_treesearch_mimic(_r[^_]+_t[^_]+)?\.p$"
    return r"\.p$"


def extract_method_label_from_filename(filename, method):
    """Extract a descriptive method label including parameters from filename.

    E.g., 'dataset_spr_r2_t0.1.p' -> 'SPR (r=2, t=0.1)'
          'dataset_subtree_t0.167.p' -> 'subtree (t=0.167)'
    """
    basename = os.path.basename(filename)
    base_name = METHOD_NAME_MAP.get(method, method)

    # Try to extract SPR parameters (radius and target)
    # Use [\d.]+ to match decimal numbers like 0.1, 0.167
    spr_match = re.search(r"_spr_r([^_]+)_t([\d.]+)\.p$", basename)
    if spr_match:
        radius, target = spr_match.groups()
        return f"{base_name} (r={radius}, t={target})"

    # Try to extract subtree parameters (target only)
    subtree_match = re.search(r"_subtree_t([\d.]+)\.p$", basename)
    if subtree_match:
        target = subtree_match.group(1)
        return f"{base_name} (t={target})"

    # Try to extract uniform/treesearch_mimic parameters
    other_match = re.search(
        r"_(uniform|treesearch_mimic)_r([^_]+)_t([\d.]+)\.p$", basename
    )
    if other_match:
        _, radius, target = other_match.groups()
        return f"{base_name} (r={radius}, t={target})"

    # Fallback to base name for old-style suffixes
    return base_name


# Plot sizing constants
DEFAULT_PLOT_HEIGHT = 4  # Minimum height for subplots in inches
HEIGHT_PER_DATASET = 1.5  # Additional height per dataset in inches
SUBPLOT_SPACING = 0.15  # Vertical spacing between subplots

# Plot axis configuration
X_AXIS_PADDING = 0.05  # Extra space on right side of x-axis
MIN_X_VALUE = -0.02  # Start x-axis slightly below zero for visual clarity
MAX_PROPORTION = 1.0  # Maximum proportion value (100%)

# Font sizes for plot elements
DEFAULT_TICK_FONTSIZE = 20  # Font size for axis tick labels
DEFAULT_LABEL_FONTSIZE = 20  # Font size for axis labels
DEFAULT_LEGEND_FONTSIZE = 20  # Font size for legend text


def load_tree_label_data(pickle_file):
    """
    Load tree and label data from a pickle file.

    Args:
        pickle_file: Path to pickle file containing tree-label dictionary

    Returns:
        dict: Dictionary with trees as keys and edge label lists as values.
              Returns empty dict if file cannot be loaded.

    Raises:
        FileNotFoundError: If pickle file does not exist
        pickle.UnpicklingError: If file cannot be unpickled
    """
    try:
        with open(pickle_file, "rb") as f:
            data = pickle.load(f)
        return data
    except FileNotFoundError:
        print(f"Error: Pickle file not found: {pickle_file}")
        raise
    except pickle.UnpicklingError as e:
        print(f"Error: Failed to unpickle file {pickle_file}: {e}")
        raise
    except Exception as e:
        print(f"Error: Unexpected error loading {pickle_file}: {e}")
        raise


def analyze_edge_distributions(data_dict, method_name, dataset_name=None):
    """
    Analyze the distribution of non-MP edges in the data.

    Args:
        data_dict: Dictionary with trees as keys and edge label lists as values
        method_name: String name of the edge distribution method
        dataset_name: Optional dataset name for multi-dataset analysis

    Returns:
        dict: Summary statistics
    """
    if not data_dict:
        return {
            "method": method_name,
            "dataset": dataset_name,
            "num_trees": 0,
            "mean_non_mp_edges": 0,
            "std_non_mp_edges": 0,
            "min_non_mp_edges": 0,
            "max_non_mp_edges": 0,
            "total_edges_per_tree": [],
            "non_mp_edge_counts": [],
            "proportion_non_mp": 0,
        }

    non_mp_counts = []
    total_edges_per_tree = []

    for tree, labels in data_dict.items():
        non_mp_count = sum(labels)  # Count of 1s (non-MP edges)
        total_edges = len(labels) - (len(tree) + 1)  # Total internal edges

        non_mp_counts.append(non_mp_count)
        total_edges_per_tree.append(total_edges)

    return {
        "method": method_name,
        "dataset": dataset_name,
        "num_trees": len(data_dict),
        "mean_non_mp_edges": np.mean(non_mp_counts) if non_mp_counts else 0,
        "std_non_mp_edges": np.std(non_mp_counts) if non_mp_counts else 0,
        "min_non_mp_edges": np.min(non_mp_counts) if non_mp_counts else 0,
        "max_non_mp_edges": np.max(non_mp_counts) if non_mp_counts else 0,
        "mean_total_edges": (
            np.mean(total_edges_per_tree) if total_edges_per_tree else 0
        ),
        "non_mp_edge_counts": non_mp_counts,
        "total_edges_per_tree": total_edges_per_tree,
        "proportion_non_mp": (
            np.mean(non_mp_counts) / np.mean(total_edges_per_tree)
            if total_edges_per_tree
            else 0
        ),
    }


def compute_longest_nonmp_path(tree, labels):
    """
    Compute the longest path of consecutive non-MP edges from root to leaf,
    normalized by the maximum tree depth.

    Args:
        tree: ete3 Tree object
        labels: List of edge labels in preorder traversal order (1 = non-MP edge)

    Returns:
        tuple: (longest_path_length, normalized_longest_path, max_tree_depth)
    """
    # Create a mapping from nodes to their edge labels
    # We need to traverse in preorder to match the labels list
    node_to_label = {}
    label_idx = 0

    # Preorder traversal to assign labels to nodes
    for node in tree.traverse("preorder"):
        if not node.is_root():
            node_to_label[node] = labels[label_idx]
            label_idx += 1

    max_path_length = 0
    max_tree_depth = 0

    # For each node, compute path lengths
    for node in tree.traverse("preorder"):
        # Compute depth (distance from root)
        depth = 0
        temp_node = node
        while not temp_node.is_root():
            depth += 1
            temp_node = temp_node.up
        max_tree_depth = max(max_tree_depth, depth)

        # Compute longest consecutive non-MP path from root
        current_path_length = 0
        current_node = node

        # Traverse from current node up to root, counting consecutive non-MP edges
        while not current_node.is_root():
            if node_to_label[current_node] == 1:
                current_path_length += 1
                current_node = current_node.up
            else:
                # Stop when we hit an MP edge (label 0)
                break

        max_path_length = max(max_path_length, current_path_length)

    # Normalize by maximum tree depth
    normalized_path = max_path_length / max_tree_depth if max_tree_depth > 0 else 0

    return max_path_length, normalized_path, max_tree_depth


def create_comparison_plots(
    analysis_results, output_dir="plots", plot_type="single_dataset"
):
    """
    Create comparison plots for different edge distribution methods.

    Args:
        analysis_results: List of analysis result dictionaries
        output_dir: Directory to save plots
        plot_type: Either "single_dataset" or "multi_dataset"
    """
    Path(output_dir).mkdir(exist_ok=True)

    # Filter out empty results
